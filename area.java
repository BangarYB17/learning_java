public class area{
    public static void main(String a[]){
        System.out.println("Area Of Circle");
        float r=4;
        double area=3.14*r*r;
        System.out.println(area);

        System.out.println("Area Of Rectangle");
        float length=4;
        float bredth=3;
        float area_rec=length*bredth;
        System.out.println(area_rec);
    }
}